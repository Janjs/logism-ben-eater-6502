code = bytearray([
  0xa9, 0xff,       # lda #$ff
  0x8d, 0x02, 0x60, # sta $6002

  0xa9, 0x55,       # lda #$55
  0x8d, 0x00, 0x60, # sta $6000

  0xa9, 0xaa,       # lda #$aa
  0x8d, 0x00, 0x60, # sta $6000

  0x4c, 0x05, 0x00, # jmp $0005
])

rom = code + bytearray([0xea] * (65536 - len(code)))

rom[0xfffc] = 0x00
rom[0xfffd] = 0x00

with open("rom.bin", "wb") as f:
  f.write(rom)